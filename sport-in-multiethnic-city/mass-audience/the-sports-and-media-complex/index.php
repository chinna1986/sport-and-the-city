    <p>A significant improvement in the television «picture» drew the attention of advertisers to sports. Already by the end of the 1980s sports as a mass-culture phenomenon lost its independent significance and became a part of the so-called «sports media complex.» This phenomenon is comprised of three mutually intertwined elements: sports, media, and advertising. The connection between them could be defined as symbiosis, or as parasitism. </p>
    <p>Television uses sports to attract a wide audience and advertising money. Sports use television to find sponsors. But the rules of the game are dictated first and foremost by television. Often this is true even in a literal sense—in order to create a better spectacle the rules of certain sports can be changed. For example, the rules of rugby have undergone 5 modifications over the last 10 years. All the significant changes, such as decreasing the number of penalties, or giving the referee the right to independently interpret the rules during a match, were directed at increasing the dynamism and speed of play.</p>

