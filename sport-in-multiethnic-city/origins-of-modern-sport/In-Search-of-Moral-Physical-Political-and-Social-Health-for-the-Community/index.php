	<p>The military defeats of the Germans during the Napoleonic wars and the defeats of the Swedes by the Russians at the beginning of the 19th century led to a reconceptualization of the gymnastics movement and the placing of political and military goals before it. Physical preparedness, together with paramilitary practices and patriotic education, would strengthen and regenerate not only an individual person, but also the entire modern community—the nation. The practical manifestation of this ideal on German lands was the creation of a series of gymnastics associations (Turnverein), inspired by the «father» of German gymnastics, Friedrich Ludwig Jahn. The concept of the education of a disciplined, organized, and healthy nation became very popular in the 19th century and gained wide diffusion throughout Central and Eastern Europe. The gymnastics movement became one of the main paths of national emancipation in the context of the Hapsburg Empire.</p>

