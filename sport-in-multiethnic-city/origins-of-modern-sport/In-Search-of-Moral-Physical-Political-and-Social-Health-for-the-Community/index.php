	<h4>Гімнастичний рух: у пошуках морального, фізичного, політичного і соціального здоров’я спільноти</h4>
	<p>Воєнні поразки німців під час наполеонівських воєн і шведів від росіян на початку ХІХ ст. сприяли переосмисленню гімнастичного руху і постановку перед ним політичних і воєнних цілей. Фізична підготовка у поєднанні з парамілітарними практиками і патріотичним вихованням сприймалася як спосіб зміцнення і регенерації не лише окремої людини, але і модерних спільнот – націй. Практичним втіленням цієї ідеї на німецьких землях стало створення мережі гімнастичних товариств (Turverein), ідейним натхненником яких був «батько» німецької гімнастики Фрідріх Людвіг Ян (Friedrich Ludwig Jahn). Концепція виховання дисциплінованої, організованої і здорової нації стала дуже популярною у ХІХ ст. й отримала широке розповсюдження у Центральній та Східній Європі. Гімнастичний рух став одним з основних засобів національної емансипації в рамках Габсбурзької імперії.</p>
	<h4>Німецька і шведська гімнастичні моделі</h4>
	<p>У 1793 р. німецький педагог Йоган Фрідріх ҐутсМутса (Johann Friedrich GutsMuths) видав книгу «Гімнастика для молоді», у якій виклав принципи гімнастичних вправ. Комплекс вправ, розроблених ҐутсМутсом, передбачав виконання вправ з допомогою спортивних снарядів. Іншу модель запропонував швед Пер Генрік Лінґ (Per Henrik Ling). Він використав ідеї Ґутсмутса, однак вважав вправи зі снарядами надто обтяжливими і невмотивовано складними. Гімнастика Лінґа базувалася на комплексі вільних вправ (без снарядів). Саме простіша шведська гімнастична модель відіграла важливу роль у випрацюванні теоретичних і методологічних основ фізичного виховання у школах.</p>
