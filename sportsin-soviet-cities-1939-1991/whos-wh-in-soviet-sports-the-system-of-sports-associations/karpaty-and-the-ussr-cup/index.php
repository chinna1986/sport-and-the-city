	<p>Завоювання «Карпатами» Кубка СРСР 1969 року – унікальний випадок в історії радянського футболу. Львівська команда – єдина, якій це вдалось здійснити, не будучи у вищій лізі СРСР, а лише у другій групі першої ліги.</p>
	<p>Кубок СРСР – футбольний турнір, особливий тим, що в ньому могли брати участь усі зареєстровані команди незалежно від їх рівня і приналежності до ліги. Впродовж сезону команди грали між собою на вибування, а фіналісти зустрічалися в столиці. У 1969 році невідомі назагал масовому всесоюзному глядачеві «Карпати» виборюють головний трофей. Львівська команда вперше грає у фіналі на найбільшому радянському стадіоні «Лужники» у Москві. Тогочасні газети пишуть, що цей фінал «важко з чимось порівняти», а традиційний обхід стадіону з трофеєм став «колом пошани львівського, карпатського і всього українського футболу».</p>
	<p>«Карпати» з капітаном команди Ігорем Кульчицьким зустрілись зі СКА (Ростов-на-Дону) і перемогли 1:2 в напруженому матчі, забивши обидва голи у другому таймі. На підтримку своєї команди влада зі Львова навіть замовила спеціальний потяг для вболівальників, переважно із заводів «Електрон», «Львівсільмаш», ЛАЗ, швейної фірми «Маяк» та ін., які привезли зі собою місцеві гасла, як-от «Кубок – на Говерлу!» Особливо зворушливим, як згадують гравці, було те, що близько 4 тисяч вболівальників під акомпанемент акордеона співали «Черемшину».</p>
	<p>«Карпати», створені 18 січня 1963 року, як і всі інші спортивні команди того часу, мусили бути прив’язані до виробництва. «Карпати» ввійшли до спортивного клубу «Авангард», а опікувався командою Львівський телевізійний завод «Електрон». Назву з локальним, а не «виробничим» акцентом надав команді голова профспілок Львівщини Гліб Клімов.</p>

