	<p>The winning of the USSR Cup by the Karpaty team in 1969 was a unique moment in the history of Soviet soccer. The Lviv team was the only team able to achieve this victory as a team not in the USSR’s highest league, but only in the second group of the first league.</p>
	<p>The particularity of the USSR Cup was that all registered teams could compete—regardless of their level or whether they belonged to the league. Throughout the season teams played each other for elimination, and the remaining finalists faced off in the capital, Moscow. In 1969 the team «Karpaty,» unknown to the All-Union mass spectator, won the ultimate trophy. The Lviv team played for the first time in the final match at the largest Soviet stadium, Luzhniki, in Moscow. Newspapers of the time wrote about how this final game was «difficult to compare with anything,» and the traditional victory march around the stadium with the trophy became a «circle of honor for Lviv, the Carpathians, and all Ukrainian soccer.»</p>
	<p>«Karpaty» with its team captain Ihor Kulchytskyi faced team SKA from Rostov-on-Don and won 1-2 in a tense match, winning both goals in overtime. To support their team the Lviv authorities even ordered a special train for fans, mostly from the factories «Elektron,» «Lvivsil’mash,» «LAZ,» the tailor firm «Maiak,» and others, which brought with them a strong local fan voice, shouting such slogans as «The Cup – to Hoverla!»—referring to the largest peak in the Carpathian mountains. Especially moving—as the players remember—was the fact that around 4 thousand fans sang the patriotic folk song «Cheremshyna» (Bird Cherry) to the accompaniment of accordions to support the team.</p>
	<p>«Karpaty,» created January 18, 1963, had to be connected to factory production--like all other sports teams. «Karpaty» entered the sports club «Avant-Garde,» but it was actually the Lviv television factory «Elektron» that took care of the team. The head of the professional unions of Lviv, Hlib Klimov, gave the team a name with a local, not a «production» accent.</p>


	
